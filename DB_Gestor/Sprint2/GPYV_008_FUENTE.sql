CREATE OR REPLACE FORCE VIEW "GPYV_008_FUENTE" ("A038CODIGO", "A038NOMBRFUNT", "A048CODIGO", "A048NOMBRE", "A047CODIGO", "A047NOMBRE", "A046CODIGO", "A046NOMBRE", "A043CODIGO", "A043NOMBRE", "A045CODIGO", "A045NOMBRE", "A038FACTOREMISION", "A038IDPROYECTO", "A038ESTADOREGISTRO") AS 
  SELECT FUE.A038CODIGO,
    FUE.A038NOMBRFUNT,
    DESA.A048CODIGO,
    DESA.A048NOMBRE,
    SUB.A047CODIGO,
    SUB.A047NOMBRE,
    CAT.A046CODIGO,
    CAT.A046NOMBRE,
	SUBG.A043CODIGO,
	SUBG.A043NOMBRE,
    SEC.A045CODIGO,
    SEC.A045NOMBRE,
    FUE.A038FACTOREMISION,
    FUE.A038IDPROYECTO,
    FUE.A038ESTADOREGISTRO
  FROM GPYT_T038_FUENTE FUE
  LEFT JOIN GPYT_T048_DESAGREGACATG_IPCC DESA
  ON FUE.A038IDDESGRCATIPCC = DESA.A048CODIGO
  LEFT JOIN GPYT_T047_SUBCATEGORIA_IPCC SUB
  ON DESA.A048IDSUBCATIPCC = SUB.A047CODIGO
  LEFT JOIN GPYT_T046_CATEGORIA_IPCC CAT
  ON SUB.A047IDCATIPCC = CAT.A046CODIGO
  LEFT JOIN GPYT_T043_SUBGRUPO_IPCC SUBG 
  ON CAT.A046IDSUBGRUPOIPCC = SUBG.A043CODIGO
  LEFT JOIN GPYT_T045_SECTOR_IPCC SEC
  ON SUBG.A043IDSECTORIPCC = SEC.A045CODIGO;
